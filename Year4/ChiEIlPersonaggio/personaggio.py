class Personaggio:
    # costruttore della classe
    def __init__(self, nome, professione, nazionalita, epoca, genere):
        # assegna il nome
        self.nome = nome
        # assegna la professione
        self.professione = professione
        # assegna la nazionalita
        self.nazionalita = nazionalita
        # assegna l'epoca
        self.epoca = epoca
        # assegna il genere
        self.genere = genere